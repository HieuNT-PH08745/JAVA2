    public QLNV() {
        initComponents();
        this.nvLst = new ArrayList<>();
        int i = 1;
        while (i<=3) {
            NhanVien nv = new NhanVien("hoten " + i, new Date(), i*1000);
            this.nvLst.add(nv);
            i++;
        }
        this.fillToTable();
    }
    
    private void fillToTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblNhanVien.getModel();
        dtm.setRowCount(0);
        for (NhanVien nv : this.nvLst) {
            Object[] rowData = new Object[]{
                nv.getHoTen(),
                this.sdf.format(nv.getNgaySinh()),
                nv.getLuong()
            };
            dtm.addRow(rowData);
        }
    }
    
    private void clear() {
        this.txtHoTen.setText(null);
        this.txtNgaySinh.setText(null);
        this.txtLuong.setText(null);
    }
    
    private NhanVien validateAndGetValue() {
        NhanVien nv = new NhanVien();
        
        String hoTen = this.txtHoTen.getText();
        String ngaySinh = this.txtNgaySinh.getText();
        String luong = this.txtLuong.getText();
        
        if (hoTen.length() == 0 || luong.length() == 0 || ngaySinh.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return null;
        }
        
        nv.setHoTen(hoTen);
        
        try {
            nv.setLuong(Double.parseDouble(luong));
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lương phải là số");
            return null;
        }
        
        try {
            nv.setNgaySinh(this.sdf.parse(ngaySinh));
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ngày sinh không đúng định dạng dd/MM/yyyy");
            return null;
        }
        
        return nv;
    }

