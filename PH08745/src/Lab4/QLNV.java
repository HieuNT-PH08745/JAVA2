    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        
        NhanVien nv = this.validateAndGetValue();
   
        if (nv == null) {
            return;
        }
        
        this.nvLst.add(nv);
        this.fillToTable();
        this.clear();
        this.row = -1;
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        
        this.row = this.tblNhanVien.getSelectedRow();
        
        if (this.row == -1) {
            return;
        }
        
        this.txtHoTen.setText(this.tblNhanVien.getValueAt(row, 0).toString());
        this.txtNgaySinh.setText(this.tblNhanVien.getValueAt(row, 1).toString());
        this.txtLuong.setText(this.tblNhanVien.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tblNhanVienMouseClicked

